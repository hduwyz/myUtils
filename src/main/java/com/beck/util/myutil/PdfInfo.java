package com.beck.util.myutil;

public class PdfInfo {

	/**
	 * 商品id
	 */
	private Integer skuId;

	/**
	 * 商品主图
	 */
	private String fgoodsItemPic;

	/**
	 * 商品名称
	 */
	private String fskuName;

	/**
	 * 商品规格
	 */
	private String fskuSpecValue;

	/**
	 * 商品数量
	 */
	private String fquantity;

	/**
	 * 条形码图片
	 */
	private String barCodePic;

	/**
	 * 条形码
	 */
	private String barCode;

	/**
	 * sku编码
	 */
	private String skuCode;

	public Integer getSkuId() {
		return skuId;
	}

	public void setSkuId(Integer skuId) {
		this.skuId = skuId;
	}

	public String getFgoodsItemPic() {
		return fgoodsItemPic;
	}

	public void setFgoodsItemPic(String fgoodsItemPic) {
		this.fgoodsItemPic = fgoodsItemPic;
	}

	public String getFskuName() {
		return fskuName;
	}

	public void setFskuName(String fskuName) {
		this.fskuName = fskuName;
	}

	public String getFskuSpecValue() {
		return fskuSpecValue;
	}

	public void setFskuSpecValue(String fskuSpecValue) {
		this.fskuSpecValue = fskuSpecValue;
	}

	public String getFquantity() {
		return fquantity;
	}

	public void setFquantity(String fquantity) {
		this.fquantity = fquantity;
	}

	public String getBarCodePic() {
		return barCodePic;
	}

	public void setBarCodePic(String barCodePic) {
		this.barCodePic = barCodePic;
	}

	public String getBarCode() {
		return barCode;
	}

	public void setBarCode(String barCode) {
		this.barCode = barCode;
	}

	public String getSkuCode() {
		return skuCode;
	}

	public void setSkuCode(String skuCode) {
		this.skuCode = skuCode;
	}
}
